  // Get current configuration
  getConfig(): EnhancedMegaAdminConfig {
    return this.config;
  }

  // Update configuration section
  updateSection(section: string, updates: any): void {
    if (this.config[section as keyof EnhancedMegaAdminConfig]) {
      this.config[section as keyof EnhancedMegaAdminConfig] = {
        ...this.config[section as keyof EnhancedMegaAdminConfig],
        ...updates
      };
    }
  }

  // Get configuration statistics
  getStats() {
    const ultraSystemCount = Object.keys(this.config.ultraSystem).length;
    const ultraTradingCount = Object.keys(this.config.ultraTrading).length;
    const ultraSecurityCount = Object.keys(this.config.ultraSecurity).length;
    const ultraAnalyticsCount = Object.keys(this.config.ultraAnalytics).length;
    const ultraInfrastructureCount = Object.keys(this.config.ultraInfrastructure).length;
    
    const totalCount = ultraSystemCount + ultraTradingCount + ultraSecurityCount + 
                      ultraAnalyticsCount + ultraInfrastructureCount;

    return {
      totalSettings: totalCount,
      sections: {
        ultraSystem: ultraSystemCount,
        ultraTrading: ultraTradingCount,
        ultraSecurity: ultraSecurityCount,
        ultraAnalytics: ultraAnalyticsCount,
        ultraInfrastructure: ultraInfrastructureCount
      },
      status: 'Enhanced Mega Admin with 5000+ Configuration Options'
    };
  }

  // Reset section to defaults
  resetSection(section: string): void {
    const defaultConfig = this.getDefaultConfig();
    if (defaultConfig[section as keyof EnhancedMegaAdminConfig]) {
      this.config[section as keyof EnhancedMegaAdminConfig] = 
        defaultConfig[section as keyof EnhancedMegaAdminConfig];
    }
  }

  // Export configuration
  exportConfig(): string {
    return JSON.stringify(this.config, null, 2);
  }

  // Import configuration
  importConfig(configJson: string): void {
    try {
      const importedConfig = JSON.parse(configJson);
      this.config = { ...this.getDefaultConfig(), ...importedConfig };
    } catch (error) {
      throw new Error('Invalid configuration format');
    }
  }

  // Validate configuration
  validateConfig(): { valid: boolean; errors: string[] } {
    const errors: string[] = [];
    
    // Basic validation checks
    if (!this.config.ultraSystem?.app_name) {
      errors.push('App name is required');
    }
    
    if (this.config.ultraSystem?.performance_concurrent_users < 1) {
      errors.push('Concurrent users must be at least 1');
    }
    
    return {
      valid: errors.length === 0,
      errors
    };
  }
