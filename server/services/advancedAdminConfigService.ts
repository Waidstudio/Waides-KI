  updateConfig(section: string, settings: any): void {
    if (this.config[section as keyof AdvancedAdminConfig]) {
      this.config[section as keyof AdvancedAdminConfig] = {
        ...this.config[section as keyof AdvancedAdminConfig],
        ...settings
      };
    }
  }

  getConfigSection(section: string): any {
    return this.config[section as keyof AdvancedAdminConfig];
  }

  resetToDefaults(section?: string): void {
    const defaults = this.getDefaultConfig();
    if (section) {
      this.config[section as keyof AdvancedAdminConfig] = defaults[section as keyof AdvancedAdminConfig];
    } else {
      this.config = defaults;
    }
  }

  exportConfig(): string {
    return JSON.stringify(this.config, null, 2);
  }

  importConfig(configData: string): boolean {
    try {
      const parsed = JSON.parse(configData);
      this.config = { ...this.getDefaultConfig(), ...parsed };
      return true;
    } catch (error) {
      return false;
    }
  }

  validateConfig(): { valid: boolean; errors: string[] } {
    const errors: string[] = [];
    
    // Add validation logic here
    if (this.config.trading.max_position_size <= 0) {
      errors.push("Trading max position size must be greater than 0");
    }
    
    if (this.config.wallet.min_deposit < 0) {
      errors.push("Wallet min deposit cannot be negative");
    }
    
    return { valid: errors.length === 0, errors };
  }
